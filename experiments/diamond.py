def analyze_diamond_data(diamond_observations):
    """
    Analyzes the raw diamond observations to determine their inherent planarity 
    and geometric consistency before passing them to the optimizer.
    """
    
    # Anchors frozen from your 2nd pass
    anchor_poses = np.array([
        [[-0.08173866, -0.08760878, -2.27461725],
         [ 3.4444503,  -2.26444289,  2.22807711]],
        [[-0.04984111,  0.02871572,  0.82210996],
         [-2.26615642,  2.69190574,  2.22807715]]
    ])

    print("=== RAW DIAMOND DATA ANALYSIS ===")

    all_points = []
    state_points = {}
    centroids = {}

    # 1. Convert all camera observations to physical room coordinates
    for state, data_arr in diamond_observations.items():
        points = []
        for c, camera_poses in enumerate(data_arr):
            for pose_cam in camera_poses:
                if pose_cam is None or np.all(pose_cam == 0):
                    continue
                pose_list = [
                    anchor_poses[c],
                    model_constants.arp_anchor_camera,
                    pose_cam,
                    gantry_april_inv
                ]
                pose_in_room = compose_poses(pose_list)
                points.append(pose_in_room[1]) # We just care about physical translation
        
        state_points[state] = points
        all_points.extend(points)
        if points:
            centroids[state] = np.mean(points, axis=0)
            print(f"State '{state}': {len(points):02d} valid observations. Centroid: {np.round(centroids[state], 3)}")

    if not all_points:
        print("No valid points found. Aborting analysis.")
        return

    all_points_arr = np.array(all_points)

    # 2. Planarity Analysis (Singular Value Decomposition)
    # The third principal component represents the normal vector of the best-fit plane.
    overall_mean = np.mean(all_points_arr, axis=0)
    centered_points = all_points_arr - overall_mean
    
    U, S, Vt = np.linalg.svd(centered_points)
    normal_vector = Vt[2]
    
    # Ensure normal points 'up' (+Z) for easier reading
    if normal_vector[2] < 0:
        normal_vector = -normal_vector

    print("\n=== PLANARITY STATS ===")
    print(f"Best-fit Plane Normal:     {np.round(normal_vector, 4)}")
    
    # Calculate how far each point strays from the best-fit plane
    distances_to_plane = np.dot(centered_points, normal_vector)
    mean_abs_err = np.mean(np.abs(distances_to_plane))
    max_err = np.max(np.abs(distances_to_plane))
    rms_err = np.sqrt(np.mean(distances_to_plane**2))
    
    print(f"Mean Absolute Deviation:   {mean_abs_err*100:.2f} cm")
    print(f"Max Deviation from Plane:  {max_err*100:.2f} cm")
    print(f"RMS Deviation from Plane:  {rms_err*100:.2f} cm")
    
    # Check verticality - a perfect pendulum swing should trace a perfectly vertical plane.
    verticality_tilt = np.degrees(np.arcsin(abs(normal_vector[2])))
    print(f"Plane Tilt from Vertical:  {verticality_tilt:.2f} degrees")

    # 3. Geometric / Distance Analysis
    print("\n=== KINEMATIC DIAMOND DISTANCES ===")
    req_states = ['bottom', 'right', 'top', 'left']
    if all(s in centroids for s in req_states):
        c_bot = centroids['bottom']
        c_rig = centroids['right']
        c_top = centroids['top']
        c_lef = centroids['left']

        # Perimeter travel distances
        dist_br = np.linalg.norm(c_rig - c_bot)
        dist_rt = np.linalg.norm(c_top - c_rig)
        dist_tl = np.linalg.norm(c_lef - c_top)
        dist_lb = np.linalg.norm(c_bot - c_lef)

        print(f"Travel Bottom -> Right:  {dist_br*100:.2f} cm")
        print(f"Travel Right  -> Top:    {dist_rt*100:.2f} cm")
        print(f"Travel Top    -> Left:   {dist_tl*100:.2f} cm")
        print(f"Travel Left   -> Bottom: {dist_lb*100:.2f} cm")
        
        # Diagonal span
        dist_bt = np.linalg.norm(c_top - c_bot)
        dist_rl = np.linalg.norm(c_lef - c_rig)
        print(f"\nDiagonal Bottom -> Top:  {dist_bt*100:.2f} cm")
        print(f"Diagonal Right  -> Left: {dist_rl*100:.2f} cm")
    else:
        print("Missing some diamond states. Cannot compute centroid distances.")

